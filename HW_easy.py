# Задача-3
# Дан произвольный список из целых чисел.
# Получите НОВЫЙ список из элементов исходного, выполнив следующие условия:
# если элемент кратен двум, то разделить его на 4, если не кратен, то умножить на два.

numbers = [1, 3, 7, 23, 5, 12, 65, 7, 2, 4, 87, 34, 32]
numbers_new = list()
for index in numbers:
    if index // 2 == 1:
        numbers_new.append(index / 4)
    else:
        numbers_new.append(index * 2)
print("Задача – 3.\nИсходный список:\n", numbers, "\nСоздан новый список, согласно условиям:\n", numbers_new)
