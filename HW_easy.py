# Задача-1:
# Дан список фруктов.
# Напишите программу, выводящую фрукты в виде нумерованного списка,
# выровненного по правой стороне.

# Пример:
# Дано: ["яблоко", "банан", "киви", "арбуз"]
# Вывод:
# 1. яблоко
# 2.  банан
# 3.   киви
# 4.  арбуз

# Подсказка: воспользоваться методом .format()

fruit = ["яблоко", "банан", "киви", "арбуз", "манго", "маракуйя", "мандарин", "груша"]
print("Задача – 1.\nИсходный список:\n", fruit, "\nСписок фруктов с форматированием:")
for index, fruit_1 in enumerate(fruit, 1):
    print(str(index) + '.', fruit_1.capitalize().rjust(len(max(fruit, key=len))))
print("\n")

